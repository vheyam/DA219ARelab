const mongoose = require('mongoose');

const albumSchema = new mongoose.Schema({
  id: {
    type: Number,
    required: true,
    unique: true,
  },
  title: {
    type: String,
    required: true,
  },
  artist: {
    type: String,
    required: true,
  },
  year: {
    type: Number,
    required: true,
  },
});

const Album = mongoose.model('Album', albumSchema);

module.exports = Album;
